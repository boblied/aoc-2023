use v5.30;

use Getopt::Long;
my $Verbose = 0;

GetOptions("verbose" => \$Verbose);

my $sum = 0;
while (<>)
{
    (my $first) = ($_ =~ m/^[^0-9]*([0-9])/);
    (my $last)  = ($_ =~ m/([0-9])[^0-9]*$/);
    $sum += "${first}${last}";
    say "$first + $last = $first$last sum=$sum" if $Verbose;
}
say $sum;
